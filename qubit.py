'''
SIMULATING QUANTUM COMPUTER COMPONENTS

'''
import random



class Electron:
    def __init__(self, state):
        self.state = state
    def getQubit():
        superposition = {
            "up" : 0,
            "down" : 1
        }
        spin = ["up", "down"]
        state = random.choice(spin)
        return superposition[state]
        






